<script>
    $(document).ready(function () {
        $('body').on('click', '.addCart', function () {
            var id = $(this).attr('data-id');
            // console.log(id);
            $.get("{{route('home.index')}}"+"/"+id+"/add", function (data) {
                $('#qtyCart').html("Cart("+data.qty+")")
                var cart = data.cart;
                console.log(cart[1].totalQty);
                $('.qtyProduct'+id).val(cart[id].totalQty)
                $('.priceProduct'+id).html(Math.round(cart[id].totalPrice)+"$")
                $('#total').html(Math.round(data.total)+"$");
                // toastr.success(data.success);
            })
        })
        $('body').on('click','.descrease', function () {
            var id = $(this).data('id');
            qty = $('.qtyProduct'+id).val()
            if(qty > 1){
                $.get("{{route('home.index')}}"+"/"+id+"/decrease", function (data) {
                    $('#qtyCart').html("Cart("+data.qty+")")
                    var cart = data.cart;
                    console.log(cart[1].totalQty);
                    $('.qtyProduct'+id).val(cart[id].totalQty)
                    $('.priceProduct'+id).html(Math.round(cart[id].totalPrice)+"$")
                    $('#total').html(Math.round(data.total)+"$");
                    $('#total').addClass()
                    // toastr.success(data.success);
                })
            }
        })
    })
</script>
